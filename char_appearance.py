def eye_shapes_pick(shape):
    if shape in ('миндалевидные', 'выразительные', 'глубоко посаженные', 'широко расставленные'):
        return shape
    return shape


def eye_shapes_cap(shape):
    if shape == 'миндалевидные':
        return 'Миндалевидные'
    if shape == 'глубоко посаженные':
        return 'Глубоко посаженные'
    if shape == 'широко расставленные':
        return 'Широко расставленные'
    if shape in ('круглые', 'раскосые', 'большие', 'узкие', 'выразительные'):
        return shape.capitalize()
    return shape


def hair_length_cap(length):
    return length.capitalize() if length else 'Длинные'


